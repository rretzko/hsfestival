                <td style="text-align: justify; font-size: 0.625rem;">
                    <p style="margin-top: -1rem;">
                        Richard DeRosa received a Grammy nomination for Best Instrumental Composition in 2015 for his big
                        band composition “Neil” which is dedicated to Neil Slater: the director of the One O’Clock Lab
                        Band at the University of North Texas from 1981-2008.
                    </p>
                    <p>
                        Since 2001 Mr. DeRosa has arranged and conducted music for Wynton Marsalis and the Jazz at
                        Lincoln Center Orchestra to feature Toots Thielemans, Annie Ross, Willie Nelson, Norah Jones,
                        Cassandra Wilson, Roberta Gambarini, and Renée Fleming among several other notable artists. He
                        was a prime arranger for the theater project (A Bed and a Chair) featuring the music of Stephen
                        Sondheim and created an arrangement of Between The Devil and the Deep Blue Sea for the swing
                        jazz Broadway show After Midnight. Mr. DeRosa was also a featured arranger for the Wynton with
                        Strings concert celebration in 2005. His most recent project as a featured conductor and
                        arranger for the LCJO was Bernstein at 100 which premiered in November of 2017.
                    </p>
                    <p>
                        In October, 2018, Mr. DeRosa was the featured conductor and arranger for the concert productions
                        of Joey Alexander with Strings which also premiered at Jazz at Lincoln Center.
                    </p>
                    <p>
                        In 2012 the WDR Big Band in Cologne, Germany, invited Mr. DeRosa to conduct and present his music
                        in concert. After several other engagements with the prestigious ensemble, he served as their
                        chief conductor and musical arranger from 2014-2016. He arranged and conducted the CD/DVD
                        recording My Personal Songbook (released in 2015) which features the music of legendary jazz
                        bassist Ron Carter who is featured with the band. A second CD titled Rediscovered Ellington
                        (released in 2017) features his longtime music partners Garry Dial and Dick Oatts. Together they
                        created unique and modern arrangements of Duke’s rare and unheard tunes. Mr. DeRosa’s newest CD
                        release (2019) is Beyond Borders which features Gregor Huebner (violin) and Richie Beirach
                        (piano) that includes new arrangements of several Beirach compositions. WDR projects with other
                        guest artists include Joshua Redman, Stefon Harris, Kurt Elling, Patti Austin, the New York
                        Voices, Ola Onabulé, Ute Lemper, Bill Mays & Marvin Stamm, and Warren Vaché.
                    </p>
                    <p>
                        Other commissioned arrangements have been recorded by the Mel Lewis, Gerry Mulligan, and Glenn
                        Miller big bands, vocalist Susannah McCorkle, trumpeter Dominick Farinacci on his CD Lovers,
                        Tales, and Dances, and acclaimed solo violinist Anne Akiko Meyers on her CD Seasons….Dreams. Mr.
                        DeRosa has also served as co-arranger, orchestrator, and conductor for the critically acclaimed
                        recording projects When Winter Comes featuring guitarist Fred Fried, Dial & Oatts: Brassworks,
                        and a double CD project That Music Always Round Me which Down Beat Magazine selected as one of
                        the top recordings in 2015. Dial & Oatts composed music to fifteen poems by Walt Whitman and
                        brought in DeRosa to create the arrangements for choir to be featured with a jazz chamber group
                        that included Dial on piano, Oatts on saxophones and flute, and guest trumpeter Terell
                        Stafford.
                    </p>
                    <p>
                        Mr. DeRosa’s arrangements for orchestra have been performed by the Kansas City Symphony, the
                        Cleveland Orchestra, the New York Pops, the Portland Maine Pops, the UNT One O’Clock Lab Band
                        with the Houston Symphony Orchestra, the Metropole Orchestra in Holland, the Czech National
                        Symphony, and the Swedish Television and Radio Orchestra in Stockholm. Other European jazz
                        bands, including the Stockholm Jazz Orchestra, have commissioned his compositions and
                        arrangements.
                    </p>
                    <p>
                        Mr. DeRosa’s compositions for television, film, and theater include background music cues for
                        Another World, As the World Turns, The Guiding Light, commercials for Telex, Bristol-Meyers, and
                        Kodak, various documentaries broadcast on PBS, orchestrations for independent films Gray
                        Matters, Falling For Grace, and Standard Time, and more than twenty original music scores for
                        the national touring U.S. theater company ArtsPower as well as orchestrations for Frankenstein,
                        the Musical. He has also composed scores for videos and hundreds of audiobooks for publishing
                        companies including Bantam Doubleday Dell, Random House, Scholastic, Simon & Schuster, and
                        Prentice-Hall.
                    </p>
                    <p>
                        Earlier in his career as a performer, DeRosa toured and recorded with Gerry Mulligan, Bob
                        Brookmeyer, Susannah McCorkle, Jackie Cain & Roy Kral, Chuck Wayne, and Marlene VerPlanck. Other
                        employers include Marian McPartland, Gene Bertoncini, Warren Vaché, Larry Elgart, Peter Nero,
                        and vocalist Chris Connor.
                    </p>
                    <p>
                        Mr. DeRosa is a recipient of UNT’s Presidential Faculty Excellence Award. In celebration of the
                        university’s 125th anniversary, he composed a work for orchestra and jazz quintet titled Suite
                        for an Anniversary. Mr. DeRosa is a full professor and the director of jazz composition and
                        arranging. His former teaching positions were at William Paterson University, Manhattan School
                        of Music, and The Juilliard School where he taught advanced jazz arranging for studio
                        orchestra.
                    </p>
                    <p>
                        He is the author of Concepts for Improvisation: A Comprehensive Guide for Performing and Teaching
                        (Hal Leonard Publications) and Acoustic and MIDI Orchestration for the Contemporary Composer
                        (Focal Press) co-authored with Dr. Andrea Pejrolo. The latter book has experienced worldwide
                        success, having been translated into Chinese in a subsequent edition. An expanded 2nd edition
                        was published in November of 2016.
                    </p>
                    <p>
                        Mr. DeRosa’s publications for public school jazz ensembles are available through Alfred Music
                        (Belwin Jazz), Smart Chart Music, J.W. Pepper, Barnhouse Music, while several of his works for
                        professional-level bands are available through Sierra Music. All of this music is available
                        through e-Jazz Lines. Mr. DeRosa remains active as an adjudicator and clinician for music
                        festivals and is the artistic director for AJV (American Jazz Venues), an organization created
                        by his late father, noted jazz education pioneer, Clem DeRosa.
                    </p>
